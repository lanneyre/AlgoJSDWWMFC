// FOR
for (let i = 1; i <= 10; i++) {
     console.log("Boucle FOR : iteration numero " + i)
}

// WHILE
let i = 1;
while (i <= 10) {
     console.log("Boucle WHILE : iteration numero " + i)
     i++;
}

// DO ... WHILE
i = 1;
do {
     console.log("Boucle DO ... WHILE : iteration numero " + i)
     i++;
} while (i <= 10);